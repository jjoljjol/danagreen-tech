let currentLang = 'en';

function toggleLanguage() {
    currentLang = currentLang === 'en' ? 'ko' : 'en';
    updateLanguage();
    document.getElementById('lang-toggle').innerText = currentLang === 'en' ? 'KO' : 'EN';
}

function updateLanguage() {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[currentLang][key]) {
            element.innerHTML = translations[currentLang][key];
        }
    });

    const placeholders = document.querySelectorAll('[data-i18n-hold]');
    placeholders.forEach(element => {
        const key = element.getAttribute('data-i18n-hold');
        if (translations[currentLang][key]) {
            element.placeholder = translations[currentLang][key];
        }
    });
}

// Custom Cursor Support
document.addEventListener('mousemove', (e) => {
    const cursor = document.getElementById('custom-cursor');
    if (cursor) {
        cursor.style.left = e.clientX + 'px';
        cursor.style.top = e.clientY + 'px';
    }
});

// Parallax Effect for Hero Background
window.addEventListener('scroll', () => {
    const scrolled = window.pageYOffset;
    const bg = document.querySelector('.parallax-bg');
    if (bg) {
        bg.style.transform = `scale(1.1) translateY(${scrolled * 0.3}px)`;
    }
});

// Intersection Observer for Section Reveal
const observerOptions = {
    threshold: 0.1
};

const revealObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            entry.target.classList.add('active');
        }
    });
}, observerOptions);

document.querySelectorAll('.section-reveal').forEach(section => {
    revealObserver.observe(section);
});

// Staggered trigger for tech cards
const techCards = document.querySelectorAll('.tech-card');
techCards.forEach((card, index) => {
    card.style.transitionDelay = `${index * 0.1}s`;
    card.classList.add('section-reveal');
    revealObserver.observe(card);
});
